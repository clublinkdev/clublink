export const parseTitleName = (message: string) => message.split("").map(char => titleChars[char as keyof typeof titleChars]).filter(Boolean).join("")

export const parseEmojis = (message: string) => message.replace(new RegExp(`(?:${Object.keys(emojilist).join("|")})`, "gu"), match => emojilist[match[0] as keyof typeof emojilist])
